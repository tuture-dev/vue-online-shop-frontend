<template>
  <div id="app">
    <router-view />
  </div>
</template>

<script>
export default {
  name: "App",
  mounted() {
    const userInfo = localStorage.getItem("userInfo");

    if (userInfo) {
      this.$store.commit("SET_USER", JSON.parse(userInfo));
    }
  }
};
</script>

